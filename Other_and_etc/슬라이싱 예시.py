k = []  # 빈 리스트 k를 생성합니다.

# 0부터 10까지의 숫자를 하나씩 반복하면서 리스트 k에 추가합니다.
for i in range(11):  # range(11)은 0부터 10까지의 숫자를 생성합니다.
    k.append(i)  # 현재 숫자 i를 리스트 k의 끝에 추가합니다.

# 리스트 k를 뒤집은 새로운 리스트 k_re를 만듭니다.
# [::-1]은 리스트를 거꾸로 슬라이싱하는 파이썬 문법입니다.
k_re = k[::-1]  

# 리스트 k와 k_re를 차례로 반복하면서 출력합니다.
for j in [k, k_re]:  # j는 먼저 k가 되고, 다음 반복에서는 k_re가 됩니다.
    print(j)        # 현재 리스트 j를 출력합니다.
    print(j * 2)    # 리스트 j를 두 번 반복한 리스트를 출력합니다.
                    # 예: [0, 1, 2] * 2 => [0, 1, 2, 0, 1, 2]

